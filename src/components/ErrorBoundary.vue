<script setup lang="ts">
import { ref, onErrorCaptured } from 'vue';
import { AnalyticsEvents } from '../utils/analytics';
import * as Sentry from '@sentry/vue';

const hasError = ref(false);
const errorMessage = ref('');
const errorStack = ref('');

// Capture errors from child components
onErrorCaptured((err, instance, info) => {
  hasError.value = true;
  errorMessage.value = err.message || 'An unexpected error occurred';
  errorStack.value = err.stack || '';

  // Track error in analytics
  AnalyticsEvents.errorBoundary(errorMessage.value);

  // Log to console in development
  if (import.meta.env.DEV) {
    console.error('[ErrorBoundary] Caught error:', {
      message: err.message,
      stack: err.stack,
      component: instance,
      info,
    });
  }

  // Send to Sentry in production
  if (import.meta.env.PROD) {
    Sentry.captureException(err, {
      contexts: {
        vue: {
          componentName: instance?.$options.name || 'Unknown',
          propsData: instance?.$props,
          lifecycleHook: info,
        },
      },
    });
  }

  // Prevent error from propagating
  return false;
});

function retry() {
  hasError.value = false;
  errorMessage.value = '';
  errorStack.value = '';
  // Force full page reload to reset all state
  window.location.reload();
}
</script>

<template>
  <div v-if="hasError" class="error-boundary">
    <div class="error-content">
      <div class="error-icon">
        <i class="fa-solid fa-triangle-exclamation" aria-hidden="true"></i>
      </div>
      <h2>Something went wrong</h2>
      <p class="error-message">{{ errorMessage }}</p>
      <div class="error-actions">
        <button class="btn btn-p" @click="retry">
          <i class="fa-solid fa-rotate-right" aria-hidden="true"></i>
          Reload Page
        </button>
        <a
          href="https://discord.gg/WnxwfMTyBe"
          target="_blank"
          rel="noopener noreferrer"
          class="btn btn-d"
        >
          <i class="fa-brands fa-discord" aria-hidden="true"></i>
          Get Help on Discord
        </a>
      </div>
      <details v-if="import.meta.env.DEV && errorStack" class="error-details">
        <summary>Technical Details (Dev Mode)</summary>
        <pre>{{ errorStack }}</pre>
      </details>
    </div>
  </div>
  <slot v-else />
</template>
